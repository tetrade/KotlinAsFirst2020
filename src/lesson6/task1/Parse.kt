fun flattenPhoneNumber(phone: String): String {
    val newNumber = Regex("""[- ]""").replace(phone, "")
    return if (newNumber.matches(Regex("""^\+?\d*\(?\d+\)?\d*"""))) {
        newNumber.replace(Regex("""[()]"""), "")
    } else ""
}

